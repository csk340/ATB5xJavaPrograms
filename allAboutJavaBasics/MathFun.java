package allAboutJavaBasics;

public class MathFun {
    public static void main(String[] args) {
        int a = -10;
        int b = 45;

        System.out.println(Math.max(a,b));//45
        System.out.println(Math.max(45.55f,67.78f));//67.78
        System.out.println(Math.pow(2,3));//8
        System.out.println(Math.min(a,b));//10
        System.out.println(Math.abs(a));
        System.out.println((10 - 4) + 3 *4);//18
        System.out.println(200 / 50 + 3 * 4);//16
        System.out.println(10+3-4);//9
    }
}
