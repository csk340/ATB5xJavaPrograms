package allAboutJavaBasics;

public class Concatination {
    public static void main(String[] args) {

        String a = "Chinmay";
        int q = 56;
        int r = 89;
        System.out.println(a+q+r);//Chinmay5689
        System.out.println(q+r+a);//145Chinmay
        System.out.println(r+a+q);//89Chinmay56
    }
}
