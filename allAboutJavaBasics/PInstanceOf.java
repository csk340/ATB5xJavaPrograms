public class PInstanceOf {
    public static void main(String[] args) {
        String s1 = "Pramod";
        System.out.println(s1 instanceof String);//s1 is part of string
        System.out.println(s1 instanceof Object);//s1 is part of object
        //Result of instanceof operator is boolean value.
    }
}
